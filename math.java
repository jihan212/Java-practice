class math {
    public static void main(String[] args) {

        // Addition math

        int sum1 = 10 + 20;
        int sum2 = sum1 + 40;
        int sum3 = sum1 + sum2;

        System.out.println("Result of Addition is " + sum3);

        // Subtraction math

        int sub1 = 200;
        int sub2 = 50;
        int sub3 = sub1 - sub2;

        System.out.println("Result of Subtraction is " + sub3);

        // Multiplication math

        int multi1 = 4 * 5;
        int multi2 = multi1 * 5;
        int multi3 = multi1 * multi2;

        System.out.println("Result of Multiplication is " + multi3);

        // Division math

        int div1 = 100 / 10;
        int div2 = div1 / 2;
        int div3 = div1 / div2;

        System.out.println("Result of Division is " + div3);

       

    }
}
